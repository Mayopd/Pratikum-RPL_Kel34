<h2>Tentang Kami</h2>

<p>
Fasiliticare adalah sistem pengaduan fasilitas yang dibuat untuk membantu
penghuni Kos Putra Griya Prospera dalam melaporkan kerusakan fasilitas kos
secara cepat dan mudah.
</p>

<p>
Melalui sistem ini, penghuni dapat melaporkan berbagai masalah seperti:
</p>

<ul>
<li>Kerusakan listrik</li>
<li>Kerusakan kamar</li>
<li>Masalah air</li>
<li>Kerusakan fasilitas umum</li>
</ul>

