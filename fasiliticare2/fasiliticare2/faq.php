<h2>FAQ (Frequently Asked Questions)</h2>

<h4>1. Bagaimana cara membuat laporan?</h4>
<p>
Isi form pada halaman utama dengan judul laporan, isi laporan,
tanggal kejadian, dan lampiran jika ada.
</p>

<h4>2. Apakah saya bisa mengirim foto kerusakan?</h4>
<p>
Ya, Anda bisa mengupload foto kerusakan melalui fitur upload lampiran.
</p>

<h4>3. Berapa lama laporan diproses?</h4>
<p>
Laporan biasanya diproses oleh pengelola kos dalam waktu 1–2 hari.
</p>

<h4>4. Apakah laporan saya bersifat rahasia?</h4>
<p>
Ya, laporan hanya dapat dilihat oleh pengelola kos.
</p>

